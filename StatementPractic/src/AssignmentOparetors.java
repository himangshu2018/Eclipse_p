
public class AssignmentOparetors {

	public static void main(String[] args) {
		
		int x=7;
		int y=3;
		
		x+=y;//x=x+y:-10
		System.out.println(x);
		
		y+=x;//y=y-x:- 13
		System.out.println(y);
		
		x*=y;//x=x*y:-130
		System.out.println(x);
		
		x/=y;//x=x/y:-10
		System.out.println(x);
		
		x%=y;//x=x%y:-0
		System.out.println(x);
		

	

	}

}
